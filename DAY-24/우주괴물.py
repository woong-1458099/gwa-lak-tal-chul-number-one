# 우주 괴물

# 문제 설명 : NxN개의 칸으로 나눠진 구역에 우주 괴물 한 마리가 침입했다. 괴물은 상하좌우로 광선을 발사하며, 벽에 막히지 않으면 광선은 계속 뻗어 나간다. 괴물의 광선이 닿지 않는 안전한 빈 칸의 수를 구하라.

# 문제 접근 방식 : "상하좌우" 키워드에 집중, 기본적인 델타 배열 활용문제

# 필요한 알고리즘 개념 : 델타배열, 인덱스

# 풀이과정
# 1 모든 괴물 좌표(2)를 미리 수집한다.  
# 2 각 괴물에서 4방향(상하좌우)으로 한 칸씩 전진
#    - 격자 밖이면 중단
#    - 벽(1) 만나면 중단
#    - 빈칸(0)이면 맞은 표시(3)로 마킹
#    - 괴물(2)나 이미 맞은 칸(3)은 값 변경 없이 통과
# 3 최종적으로 값이 0인 칸의 개수(=안전한 칸)를 센다.



T = int(input())

# 4방 델타(상, 하, 좌, 우)
di = [-1, 1, 0, 0]
dj = [0, 0, -1, 1]

for tc in range(1, T + 1):
    # N x N 
    N = int(input())

    # 격자 입력
    # 0=빈 칸, 1=벽, 2=우주괴물
    space = [list(map(int, input().split())) for _ in range(N)]

    # 모든 칸을 순회하면서 우주괴물(2)을 찾는다
    # 찾은 위치에서 상하좌우로 광선을 쏘며 마킹(3)한다
    for i in range(N):
        for j in range(N):
            if space[i][j] == 2:
                # 이 지점(i, j)에서 4방향으로 전진
                for d in range(4):
                    # k: 현재 방향으로 몇 칸 전진했는지(0부터 N-1까지)
                    # 주의) k=0이면 '현재 위치(괴물 자리)'도 포함됨
                    #      원문 로직은 괴물 자리도 3으로 마킹하게 됨
                    for k in range(N):
                        ni = i + di[d] * k
                        nj = j + dj[d] * k
                        # 격자 범위 안이고, 벽(1)이 아니라면 계속 진행
                        if 0 <= ni < N and 0 <= nj < N and space[ni][nj] != 1:
                            # 광선이 닿았다 -> 3으로 마킹
                
                            space[ni][nj] = 3
                        else:
                            # 격자 밖이거나 벽(1)을 만나면 해당 방향 종료
                            break

    # 안전한 빈 칸(0인 칸) 개수 세기
    safe_count = 0
    for i in range(N):
        for j in range(N):
            if space[i][j] == 0:
                safe_count += 1

   
    print(f"#{tc} {safe_count}")
