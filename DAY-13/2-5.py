max_len = 1 # 최소 길이가 1 (자기 자신)

for r in range(N):   # 각 행마다 (row)
    row = matrix[r]
    for i in range(N):   # 각 칸을 '중심'으로 잡고
        radius = 0
        while True:  # 무한루프 시작
            Left = i - radius
            Right = i + radius
            if Left < 0 or Right >= N: # 범위를 벗어날 떄
                break
            if row[Left] != row[Right]: # 대칭이 아닐 때
                break
            cur_len = 2 * radius + 1  # 이게 홀수 길이
            if cur_len > max_len:
                max_len = cur_len
            radius += 1

